<p><strong>Порядок тестирования</strong></p>
<p class="indent text-justify">1. Зарегистрируйтесь у сервис-партнёра.</p>
<p class="indent text-justify">2. После регистрации вы получите логин и пароль для входа.</p>
<p class="indent text-justify">3. Авторизируйтесь у сервис-партнёра, введя логин и пароль.</p>
<p class="indent text-justify">4. Найдите пункт «Cвязать с аккаунтом FARKeys».</p>
<p class="indent text-justify">5. Найдите подпункт «Привязать новый FARKeys ID».</p>
<p class="indent text-justify">6. Укажите необходимые данные и осуществите привязку.</p>
<p class="indent text-justify">7. Выйдите из личного кабинета.</p>
<p class="indent text-justify">8. Найдите и нажмите кнопку «FARKeys Авторизация».</p>
<p class="indent text-justify">9. Пройдите идентификацию на сайте FARKeys.</p>
<p class="indent text-justify">10. Вы успешно авторизированы :)</p>
